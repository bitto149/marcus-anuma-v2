// Gestisce l'ottimizzazione e l'arricchimento dell'interfaccia grafica utente

export function enhanceUI(container) {
    container.style.transition = "all 0.3s ease-in-out";
    container.style.borderRadius = "10px";
    container.style.boxShadow = "0 4px 8px rgba(0,0,0,0.2)";
    container.style.background = "linear-gradient(to right, #e0eafc, #cfdef3)";
    console.log("Interfaccia utente migliorata visivamente.");
}
